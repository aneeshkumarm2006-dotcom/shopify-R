function variant(
  id: string,
  title: string,
  sku: string,
  price: number,
  qty: number,
  threshold: number,
  policy: "deny" | "continue",
  compareAtPrice: number | null = null,
): Variant {
  return {
    id,
    title,
    sku,
    barcode: `8${sku.replace(/[^0-9A-Z]/g, "").padEnd(11, "0").slice(0, 11)}`,
    price,
    compareAtPrice,
    inventory: { quantity: qty, policy, lowStockThreshold: threshold, trackInventory: true },
  };
}

